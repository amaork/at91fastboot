#define set_lcd_rs() 	sys_write(AT91C_PIOB_SODR, 0x40)
#define clr_lcd_rs() 	sys_write(AT91C_PIOB_CODR, 0x40)
#define set_lcd_en() 	sys_write(AT91C_PIOB_SODR, 0x80)
#define clr_lcd_en() 	sys_write(AT91C_PIOB_CODR, 0x80)
#define set_lcd_csa()	sys_write(AT91C_PIOA_SODR, 0x100)
#define clr_lcd_csa() 	sys_write(AT91C_PIOA_CODR, 0x100)
#define set_lcd_csb()	sys_write(AT91C_PIOA_SODR, 0x200)
#define clr_lcd_csb()	sys_write(AT91C_PIOA_CODR, 0x200)

static char logo[];

static void lcd_delay(int count)
{
	volatile int i;
	for(i=0; i<count; i++);
}

static void lcd_cmd(char cs, unsigned int cmd)
{
	*(volatile unsigned int *)0x30000000=(cmd<<24);
	clr_lcd_en();
	clr_lcd_rs();
	
	if(cs==0) 
	{
		clr_lcd_csa();
		clr_lcd_csb();
	}
	if(cs==1) 
	{
		clr_lcd_csa();
		set_lcd_csb();
	}
	if(cs==2) 
	{
		set_lcd_csa();
		clr_lcd_csb();
	}

	lcd_delay(10);
	set_lcd_en();
	lcd_delay(10);
	clr_lcd_en();
	lcd_delay(2);

	set_lcd_csa();
	set_lcd_csb();
	lcd_delay(85);
}

static void lcd_data(char cs, unsigned int data)
{
	*(volatile unsigned int *)0x30000000=(data<<24);
	clr_lcd_en();
	set_lcd_rs();
	
	if(cs==0) 
	{
		clr_lcd_csa();
		clr_lcd_csb();
	}
	if(cs==1) 
	{
		clr_lcd_csa();
		set_lcd_csb();
	}
	if(cs==2) 
	{
		set_lcd_csa();
		clr_lcd_csb();
	}

	lcd_delay(10);
	set_lcd_en();
	lcd_delay(10);
	clr_lcd_en();
	lcd_delay(2);

	set_lcd_csa();
	set_lcd_csb();
	lcd_delay(85);
}

static void lcd_hw_init(void)
{
	const struct pio_desc lcd_pio[] = {
		
		/* 	Enable NCS2 LCD DB0-7 Using 0x30000000 height 8bits need enable NCS2 */
		{"NCS2", AT91C_PIN_PC(11), 0,	PIO_PULLUP, 	PIO_PERIPH_A},

		/*	LCD 3 chipselect pin */
		{"CSA",	 AT91C_PIN_PA(8),  0, 	PIO_DEFAULT,  	PIO_OUTPUT},
		{"CSB",	 AT91C_PIN_PA(9),  0, 	PIO_DEFAULT,  	PIO_OUTPUT},
		{"CSC",	 AT91C_PIN_PA(10), 0, 	PIO_DEFAULT,  	PIO_OUTPUT},

		/* 	LCD en and rs pin */
		{"RS",	 AT91C_PIN_PB(6),  0, 	PIO_DEFAULT,  	PIO_OUTPUT},
		{"EN",	 AT91C_PIN_PB(7),  0, 	PIO_DEFAULT,  	PIO_OUTPUT},
        {(char *)0, 0, 0, PIO_DEFAULT, PIO_PERIPH_A},
	};


	pio_setup(lcd_pio);	

	lcd_cmd(0, 0x3f);
	lcd_cmd(1, 0x3f);
	lcd_cmd(2, 0x3f);
	lcd_cmd(0, 0xc0);
	lcd_cmd(1, 0xc0);
	lcd_cmd(2, 0xc0);
}

void lcd_19264_draw_logo(void)
{
	int i, j, k, n=0;
		
	lcd_hw_init();

	for(i = 0; i < 8; i++){

		for(j = 0; j < 3; j++){

			lcd_cmd(j, 0x40);
			lcd_cmd(j, 0xb8|i);

			for(k = 0; k < 64; k++)
				lcd_data(j, logo[n++]);
		}
	}
}

